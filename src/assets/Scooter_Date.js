const createProduct = ({
  id,
  title,
  category,
  features = [],
  audience = [],
  price,
  stock,
  description,
  topSeller = false,
  img = "",
  badge,
  battery = "2000 mAh",
  power = "1,2 л.с.",
  speed = "60 km/ч"
}) => {
  const categoryMeta =
    SCOOTER_CATEGORIES.find((item) => item.value === category) ||
    SCOOTER_CATEGORIES[0];

  return Object.freeze({
    id,
    title,
    categoryKey: categoryMeta.value,
    category: categoryMeta.label,
    features,
    audience,
    audienceLabels: audience.map((segment) => SCOOTER_AUDIENCE[segment] || segment),
    price,
    stock,
    description,
    topSeller,
    img,
    badge,
    battery,
    power,
    speed
  });
};

